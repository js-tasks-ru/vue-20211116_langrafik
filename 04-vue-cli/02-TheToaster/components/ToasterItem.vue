<template>
  <div class="toast" :class="toastClass">
    <ui-icon class="toast__icon" :icon="toastIcon" />
    <span>{{ message }}</span>
  </div>
</template>

<script>
import UiIcon from './UiIcon';

export default {
  name: 'ToasterItem',
  components: { UiIcon },
  props: {
    toastType: {
      type: String,
      default: 'success',
    },
    message: {
      type: String,
      default: '',
    },
  },
  computed: {
    toastClass() {
      return this.toastType === 'success' ? 'toast_success' : 'toast_error';
    },
    toastIcon() {
      return this.toastType === 'success' ? 'check-circle' : 'alert-circle';
    },
  },
};
</script>

